const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

// module.exports = new mongoose.model("user", UserSchema);
const User = new mongoose.model("user", UserSchema);

module.exports = {
  User,
  UserSchema,
};
